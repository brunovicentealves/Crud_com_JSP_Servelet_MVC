package Modelo;

/**
 *
 * @author bruno.alves
 */
public class Pessoas {

    private int id;
    private String nome;
    private String Cpf;

    public Pessoas() {

    }

    public Pessoas(String nome, String Cpf) {
        
        this.nome = nome;
        this.Cpf = Cpf;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return Cpf;
    }

    public void setCpf(String Cpf) {
        this.Cpf = Cpf;
    }
    
    

}
